package clases.data_types;
/**
 * Programa para definir e inicializar variables de distintos tipos
 * de Java.
 * 
 * @author <a href="mailto:mauricio.rodriguez@globant.com">mauricio.rodriguez</a>
 *
 */
public class TiposDeDatos {

    public static void main(String[] args) {
        
        // Definicion de un String
        String miString = "Hola Mundo!!";
        System.out.println("miString = " + miString); 
        
        // Definicion de un entero
        int miEntero = 155;
        System.out.println("miEntero = " + miEntero);
        
        // Definicion de un short
        short miEnteroCorto = 10;
        System.out.println("miEnteroCorto = " + miEnteroCorto);
        
        // Definicion de un long
        long miEnteroLargo = 469221911;
        System.out.println("miEnteroLargo = " + miEnteroLargo);
        
        // Definicion de un float
        float miReal = 2.5f;
        System.out.println("miReal = " + miReal);
        
        // Definicion de un character
        char miCaracter = 'K';
        System.out.println("miCaracter = " + miCaracter);
        
        // Definicion de booleano
        boolean miBooleano = false;
        System.out.println("miBooleano = " + miBooleano);
    }

}
